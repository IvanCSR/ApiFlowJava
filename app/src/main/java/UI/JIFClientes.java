    private void jbtneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtneliminarActionPerformed
        if (jtbLista.getSelectedRow() >= 0) {
            var indice = jtbLista.getSelectedRow();
            var id = Integer.parseInt(jtbLista.getValueAt(indice, 0).toString());
            var nombres = jtbLista.getValueAt(indice, 1).toString();
            var apellidos = jtbLista.getValueAt(indice, 2).toString();
            var dni = jtbLista.getValueAt(indice, 3).toString();
            var genero = jtbLista.getValueAt(indice, 4).toString();
            var direccion = jtbLista.getValueAt(indice, 5).toString();
            cliente = new Clientes(id, nombres, apellidos, dni, genero, direccion);
            if (clienteManejador.Eliminar(cliente)) {
                Notifications.getInstance().show(Notifications.Type.SUCCESS, Notifications.Location.TOP_CENTER, JDClientesDatos.texto);
            }
        }
    }//GEN-LAST:event_jbtneliminarActionPerformed


